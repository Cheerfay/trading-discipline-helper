import type { LLMMessage } from './llm';
import type { PositionCardInput, PositionRuleSummary } from './types';

export const POSITION_CARD_SYSTEM_PROMPT = `你是「交易冷静卡」里的仓位纪律陪伴者。

你的任务不是判断某只股票好不好，也不是判断现在该不该买卖，而是把用户写出的仓位安排翻译成一张「仓位健康检查 / 建仓节奏卡」。

合规红线，必须严守：
- 不预测涨跌，不给买卖建议，不给目标价
- 不评价用户提到的具体标的值不值得买、该不该持有、未来怎么走
- 不给针对这只标的的具体仓位建议数字；不要说"买到多少%"、"减到多少%"、"加几成"、"卖一半"
- 不给择时阈值；不要说"突破某价位/涨跌某百分比就买卖"
- 不使用"建议买入/卖出/加仓/减仓/抄底/逃顶/重仓/稳赚/必涨"等表达

你可以做：
- 复述用户自己写出的仓位数字和事实
- 用自然语言解释代码规则给出的当前仓位档位，比如偏轻、相对合理、偏重、过度集中
- 提醒通用仓位纪律：先写总上限、分批执行、保留现金缓冲、把同主题风险合并看
- 给一个小动作，让用户先停下来整理，而不是立刻交易

表达要求：
- 规则结果只作为内部风控口径，不要像系统打分或审判用户
- 可以引用用户当前所在档位的风控含义，但不要列完整阈值表
- 不要说"根据规则"、"按照规则"、"5%-20%区间"这类话
- 用"整体账户波动"、"账户结果"、"整体持仓"解释仓位影响

语气：冷静、克制、像理性的朋友。不要吓人，不要审判。只输出 JSON 对象，不要额外解释。`;

export function buildPositionPrompt(input: PositionCardInput, summary: PositionRuleSummary): LLMMessage[] {
  const prompt = `用户刚生成了一张交易冷静卡，现在想进一步看看仓位稳不稳。

用户原始想法：
${input.userThought || '（未提供）'}

用户补充的仓位描述：
${input.positionText || '（未提供）'}

用户提到的标的：
${input.symbol || '（未填写）'}

代码规则已经完成的内部风控检查结果（这是内部口径，你不能推翻，但对用户表达时不要复述完整阈值表）：
${JSON.stringify(summary, null, 2)}

请生成一张「仓位健康检查 / 建仓节奏卡」，按下面 JSON 输出：

\`\`\`json
{
  "headline": "首屏主文案，2-4句。先接住用户想看仓位稳不稳的需求，再复述用户自己写出的关键数字或事实，然后用自然语言说明当前仓位对整体账户波动的影响。不能给买卖建议，不能给新的仓位数字；不要列完整阈值表。",
  "rhythmInsight": "一句话说明当前仓位节奏的核心问题，例如是单票集中、节奏太急、现金缓冲不足、同主题集中，或信息不足。",
  "oneAction": "一个现在就能做的小动作。只能是通用纪律动作，例如先写下总上限、把同主题持仓合并看、把这次操作拆成观察清单；不要写具体买卖比例或时机。",
  "checkpoints": ["3个自查问题，每个一句话，围绕仓位、节奏、现金缓冲或同主题风险。"],
  "notes": ["最多3条补充说明。解释规则结果和仓位纪律，不评价标的。"]
}
\`\`\`

注意：
- 所有数字只能来自用户原文或代码规则结果，不要编造。
- 如果信息不足，要直接说"还缺仓位比例/总资产占比"，但仍然给一张有用的节奏卡。
- 不要输出完整阈值表，不要用"根据规则/按照规则/某某区间"的口吻。
- 只返回 JSON。`;

  return [{ role: 'user', content: prompt }];
}
